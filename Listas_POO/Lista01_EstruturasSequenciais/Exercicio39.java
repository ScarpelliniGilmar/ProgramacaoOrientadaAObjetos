package Lista;

import javax.swing.JOptionPane;

/*
 * 39. João recebeu seu salário de R$ 1200,00 e precisa pagar
 *  duas contas (C1=R$ 200,00 e C2=R$120,00) que estão atrasadas.
 *   Como as contas estão atrasadas, João terá de pagar multa de 
 *   2% sobre cada conta. Faça um algoritmo que calcule e mostre 
 *   quanto restará do salário do João 
 */

public class Exercicio39 {
	public static void main(String[] args) {
	
		double salario = 1200;
		double c1 = 200*1.02;
		double c2 = 120*1.02;
		double restoSalario = salario-(c1+c2);

		JOptionPane.showMessageDialog(null, "Salário R$"+salario+
				"\nConta 1 com jutos R$ "+c1+
				"\nConta 2 com jutos R$ "+c2+
				"\nResto do salário R$ "+restoSalario);
				
		
		
	}

}
